public class ASSIGNDEMO {
    public static void main(String[] args) {
        int x=2;
        int y=5;

        x+=y;
        System.out.println("x : "+x);
        x-=y;
        System.out.println("x : "+x);
        x*=y;
        System.out.println("x : "+x);
        x/=y;
        System.out.println("x : "+x);
        x%=y;
        System.out.println("x : "+x);
    }
}
